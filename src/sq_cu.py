#Write a Python program to print the square and cube symbol in the area of a rectangle and volume of a cylinder.

area = 1256.66
volume = 1254.725
decimals = 2
print("The area of the rectangle is {:.{}f}cm\u00b2".format(area,decimals))
decimals = 3
print("The volume of the cylinder is {:.{}f}cm\u00b3".format(volume,decimals))
